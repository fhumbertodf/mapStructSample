package com.tutorialspoint.model;

public class Car {

	private int id;
	private String price;
	private String manufacturingDate1;
	private String manufacturingDate2;
	private String brand;
	private String name1;
	private String name2;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getManufacturingDate1() {
		return manufacturingDate1;
	}

	public void setManufacturingDate1(String manufacturingDate1) {
		this.manufacturingDate1 = manufacturingDate1;
	}

	public String getManufacturingDate2() {
		return manufacturingDate2;
	}

	public void setManufacturingDate2(String manufacturingDate2) {
		this.manufacturingDate2 = manufacturingDate2;
	}

	public String getName1() {
		return name1;
	}

	public void setName1(String name1) {
		this.name1 = name1;
	}

	public String getName2() {
		return name2;
	}

	public void setName2(String name2) {
		this.name2 = name2;
	}
}
